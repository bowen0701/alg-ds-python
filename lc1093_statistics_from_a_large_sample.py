class Solution(object):
    def _selectKth(self, k, counts):
        cusum = 0
        for i in range(len(counts)):
            cusum += counts[i]
            if k <= cusum:
                return i
        
    def sampleStats(self, counts):
        """
        :type counts: List[int]
        :rtype: List[float]

        Time complexity: O(m), where m is the length of counts.
        Space complexity: O(1).
        """
        n = sum(counts)
        n_counts = len(counts)

        # Iterate through counts to update minimum.
        minimum = 0
        for i in range(n_counts):
            if counts[i]:
                minimum = float(i)
                break

        # Iterate through counts to update maximum.
        maximum = n_counts - 1  
        for i in range(n_counts - 1, -1, -1):
            if counts[i]:
                maximum = float(i)
                break

        # Iterate through counts to compute total and mean. 
        total = 0
        for i in range(1, n_counts):
            total += i * counts[i]
        mean = float(total) / n

        # Iterate through counts to find median.
        if n % 2:
            median = float(self._selectKth(n // 2 + 1, counts))
        else:
            median = float(self._selectKth(n // 2, counts) 
                           + self._selectKth(n // 2 + 1, counts)) / 2

        # Iterate through counts to compute mode.
        mode = 0
        mode_count = counts[0]
        for i in range(1, n_counts):
            if counts[i] > mode_count:
                mode = float(i)
                mode_count = counts[i]

        return [minimum, maximum, mean, median, mode]


